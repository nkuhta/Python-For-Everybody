
##################################################
##############  Variable assignment  #############
##################################################

#  assign the value 12 to variable a
a=12
#  now add 2 to a (so now a=14)
a=a+2
#  print out the current value of a+2
print("a+2 =",a+2)

#  more complicated assignment operator
x=3.9*a*(1-a)
print("x =",x)

##################################################
############  Numeric Operators  #################
##################################################

    #   +   = Addition
    #   -   = Subtraction
    #   *   = Multiplication
    #   /   = Division
    #   **  = Power
    #   %   = Remainder

#  Addition/Subtraction
xx = 2
xx = xx + 2
print("xx =",xx)
print("xx-2 =",xx-2)

#  Multiplication/Division
yy=440*12
print('yy =',yy)
zz=yy/1000
print('zz =',zz)

#  Remainder
jj=23
kk=jj % 5
print("23/5 Remainder = ",kk)

#  Power
print("4^3 = ",4**3)
